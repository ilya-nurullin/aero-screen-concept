  .row
    .col-md-4
      ul.list-group
        li.list-group-item.list-group-item-action(@click="show('maps')") Карты
        li.list-group-item.list-group-item-action(@click="show('aboutFlight')") О полёте
        li.list-group-item.list-group-item-action(@click="show('entertainment')") Развлечения
        li.list-group-item.list-group-item-action(@click="show('settings')") Настройки
        li.list-group-item.list-group-item-action(@click="show('steward')") Вызов бортпроводника
    .col-md-8
      .card
        .card-header {{ title }}
        .card-block(v-html="body")

    section#start(data-title="Начало") Для начала выберите пункт в левом меню.

    section#maps(data-title="Карты")
      b Найти место
      p Чтобы отыскать необходимое Вам место, необходимо воспользоваться 2ГИС или Картыми Гугл. 2ГИС лучше всего поможет Вам ориентироваться по городу, в который Вы направляетесь. С помощью 2ГИС можно отыскать не. Только улицу, но и определеную организацию или магазин. Карты Гугл понадобятся Вам, если вы хотите ориентировать не только по городу назначения, но и проложить дальнейший маршрут. Также Карты Гугл помогут Вам определить Ваше текущее местоположение

      b Проложить маршрут
      p Чтобы проложить маршрут в городе назначения и его окрестностях, рекомендуем воспользоваться 2ГИС. Для начала необходимо отметить пункт отправления, а затем пункт прибытия. 2ГИС сам проложит необходимый маршрут. Для более продолжительных маршрутов советуем воспользоваться Картами Гугл. Вам так же необходимо отметить пункт отправления и пункт прибытия. Карты помогут Вам ориентироваться не только по городу, но и по всему миру.

