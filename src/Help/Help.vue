<style scoped>
  .row {
    margin-top: 1rem;
    padding: 0 1rem;
  }
  section {
    display: none;
  }
</style>

<script>
  export default {
    mounted() {
      $('li.list-group-item.list-group-item-action').click(function () {
        $('li.list-group-item.list-group-item-action').removeClass('active');
        $(this).addClass('active');
      });
      this.currentId = 'start';
    },
    data: () => {
      return {
        currentId: null
      }
    },
    computed: {
      title: function() {
        return $(`section#${this.currentId}`).data('title');
      },
      body: function() {
        return $(`section#${this.currentId}`).html();
      }
    },
    methods: {
      show: function (id) { 
        this.currentId = id;
      }
    }
  }
</script>